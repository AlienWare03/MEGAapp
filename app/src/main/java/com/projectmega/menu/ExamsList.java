public class ExamsList {

    //Data variables
    private String subjectTitle;
    private String examName;
    private String examScore;
    private String examItems;
    private String examPercent;

    public String getSubjectTitle(){
        return subjectTitle;
    }
    public void setSubjectTitle(String subjectTitle){
        this.subjectTitle = subjectTitle;
    }
    public String getExamName(){
        return examName;
    }
    public void setExamName(String examName){
        this.examName = examName;
    }
    public String getExamScore(){
        return examScore;
    }
    public void setExamScore(String examScore){
        this.examScore = examScore;
    }
    public String getExamItems(){
        return examItems;
    }
    public void setExamItems(String examItems){
        this.examItems = examItems;
    }
    public String getExamPercent(){
        return examPercent;
    }
    public void setExamPercent(String examPercent){
        this.examPercent = examPercent;
    }



}
